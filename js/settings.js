        _removePhoto: async function (container) {
            var self = this;
            var uid = PharmaFlow.Auth && PharmaFlow.Auth.currentUser ? PharmaFlow.Auth.currentUser.uid : null;
            if (!uid) return;

            try {
                var prevUrl = PharmaFlow.Auth.userProfile && PharmaFlow.Auth.userProfile.photoURL;

                // Remove from Firestore
                await window.db.collection('users').doc(uid).update({
                    photoURL: firebase.firestore.FieldValue.delete(),
                    updatedAt: new Date().toISOString()
                });

                // Delete from Firebase Storage only if the photo was stored there
                if (prevUrl && PharmaFlow.CloudinaryUpload && PharmaFlow.CloudinaryUpload.isFirebaseStorageUrl(prevUrl)) {
                    try {
                        var extensions = ['png', 'jpeg', 'jpg', 'webp'];
                        for (var i = 0; i < extensions.length; i++) {
                            try {
                                await window.storage.ref('users/' + uid + '/profile_photo/avatar.' + extensions[i]).delete();
                            } catch (e) { /* file may not exist with this extension */ }
                        }
                    } catch (e) { /* ignore storage errors */ }
                }

                // Update local profile
                delete PharmaFlow.Auth.userProfile.photoURL;

                // Revert to initials
                var displayName = PharmaFlow.Auth.userProfile.displayName || 'User';
                var avatarEl = document.getElementById('st-avatar-display');
                if (avatarEl) avatarEl.innerHTML = self._getInitials(displayName);

                // Update topbar avatar
                var topAvatar = document.getElementById('profile-avatar');
                if (topAvatar) {
                    var initials = displayName.split(' ').map(function (n) { return n[0]; }).join('').substring(0, 2).toUpperCase();
                    topAvatar.innerHTML = '<span>' + initials + '</span>';
                }

                // Remove the remove button
                var removeBtn = document.getElementById('st-photo-remove');
                if (removeBtn) removeBtn.remove();

                self._showToast('Profile photo removed', 'success');
            } catch (err) {
                console.error('Remove photo failed:', err);
                self._showToast('Failed to remove photo', 'error');
            }
        },


